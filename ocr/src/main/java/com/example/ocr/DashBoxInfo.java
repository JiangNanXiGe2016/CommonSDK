package com.example.ocr;

public class DashBoxInfo {
    float startLeftX;
    float startLeftY;

    float endRightX;
    float endRightY;

    @Override
    public String toString() {
        return "DashBoxInfo{" +
                "startLeftX=" + startLeftX +
                ", startLeftY=" + startLeftY +
                ", endRightX=" + endRightX +
                ", endRightY=" + endRightY +
                '}';
    }
}
